package ru.spbau.shavkunov;

/**
 * Интерфейс для ленивого вычисления.
 * @param <T> параметр вычисления.
 */
public interface Lazy<T> {
    /**
     * Метод позволяет получить результат вычисления.  Первый вызов производит вычисление,
     * а дальнейшие вызовы возвращают ту же ссылку, что и при первом вычислении.
     * @return результат вычисления.
     */
    T get();
}